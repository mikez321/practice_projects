"""This is a lesson on string replacements."""
age = 24
print(type(age))
print("My name is " + str(age) + " years.")
print("My name is {0} years.".format(age))

print("There are {0} days in {1}, {2}, {3}, {4}, {5}, {6}, and {7}."
      .format(31, "Jan", "March", "May", "July", "Aug", "Oct", "Dec"))
