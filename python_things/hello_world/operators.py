"""This is a lesson using python int data types."""
a = 12
b = 3
print(a + b)
print(a - b)
print(a * b)
print(a / b)  # Interesting... this will produce a float!
print(a // b)  # integer division (rounded down)
print(a % b)  # modulo works the same as in Ruby

print()

for i in range(1, 4):
    print(i)
